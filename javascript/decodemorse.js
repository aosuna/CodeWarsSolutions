decodeMorse = function (morseCode) {
    let message = '';
    let words = morseCode.split('   ').filter(x => x !== '');
    for (let i = 0; i < words.length; i++) {
        if (message.length > 0) {
            message += ' ';
        }
        message += words[i].split(' ').map(letter => MORSE_CODE[letter]).join('');
    }
    return message;
}

